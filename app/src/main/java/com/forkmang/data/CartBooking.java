package com.forkmang.data;

public class CartBooking {

    String str_productname;
    String str_qty;
    String str_type;


    public String getStr_productname() {
        return str_productname;
    }

    public void setStr_productname(String str_productname) {
        this.str_productname = str_productname;
    }

    public String getStr_qty() {
        return str_qty;
    }

    public void setStr_qty(String str_qty) {
        this.str_qty = str_qty;
    }

    public String getStr_type() {
        return str_type;
    }

    public void setStr_type(String str_type) {
        this.str_type = str_type;
    }
}
